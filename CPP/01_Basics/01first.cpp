// This program demonstrates the basic structure of a C++ program. 
// It includes essential components like headers, the `main` function, 
// and a simple output statement.

// Include the iostream header file for input/output operations.
#include<iostream>

// Use the standard namespace to simplify code writing and avoid prefixing `std::`.
using namespace std;

// The main function serves as the entry point of the program.
